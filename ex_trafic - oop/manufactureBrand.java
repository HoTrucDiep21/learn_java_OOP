

public class manufactureBrand {
    String manufactureBrandName, countryName;

    public manufactureBrand() {
    }

    public manufactureBrand(String manufactureBrandName, String countryName) {
        this.manufactureBrandName = manufactureBrandName;
        this.countryName = countryName;
    }

    public String getManufactureBrandName() {
        return manufactureBrandName;
    }

    public void setManufactureBrandName(String manufactureBrandName) {
        this.manufactureBrandName = manufactureBrandName;
    }

    public String getCountryName() {
        return countryName;
    }

    public void setCountryName(String countryName) {
        this.countryName = countryName;
    }

    

}
